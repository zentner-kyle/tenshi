uint8_t received_ids[ID_COUNT][ID_LEN];

void print_id(const char * prefix, uint8_t id[ID_LEN]) {
  printf("%s0x", prefix);
  for (int j = 0; j < ID_LEN; j++) {
    printf("%x", id[j]);
  }
  printf("\n");
}


bool deviceExists(uint8_t mask[ID_LEN], uint8_t value[ID_LEN]) {
  ++time;
  print_id("mask =", mask);
  for (int i = 0; i < ID_COUNT; i++) {
    if (ids[i][ID_LEN]) {
      // Disabled.
      continue;
    }
    bool eq = true;
    for (int j = 0; j < ID_LEN; j++) {
      if ((ids[i][j] & mask[j]) != value[j]) {
        eq = false;
      }
    }
    if (eq) {
      /*printf("id \t= %zx\n", *(uint64_t *) ids[i]);*/
      /*printf("value \t= %zx\n", *(uint64_t *) value);*/
      /*printf("mask \t= %zx\n", *(uint64_t *) mask);*/
      for (int j = 0; j < ID_LEN; j++) {
        printf("%x & %x == %x\n", ids[i][j], mask[j], value[j]);
      }
      return true;
    }
  }
  return false;
}

void disableDevice(uint8_t id[ID_LEN]) {
  ++time;
  for (int i = 0; i < ID_COUNT; i++) {
    if (memcmp(id, ids[i], 8) == 0) {
      ids[i][ID_LEN] = 0x01;
      return;
    }
  }
  uint8_t mask[ID_LEN];
  toArray(mask, ~0L);
  assert(!deviceExists(mask, id));
  print_id("mask \t= ", mask);
  print_id("id \t= ", id);
  printf("Attempt to disable non-existent device.\n");
}

bool resetAllDevices() {
  for (int i = 0; i < ID_COUNT; i++) {
    ids[i][ID_LEN] = 0x0;
  }
}

